"""
gym.py - 체육관(시장 국면) 데이터를 정의하는 파일

[v0.3] 이제 가짜 데이터가 아니다. 각 체육관은 실제 역사적 기간을 가리키고,
battle.py가 그 기간의 SPY(S&P500 ETF) 가격을 받아 진짜 백테스트를 돌린다.

[difficulty / volatility]
  이제 판정에는 안 쓰는 '연출용 메타데이터'다(시장 설명·정렬 참고용, 1~10 척도).
  실제 난이도는 그 기간 가격이 직접 만든다.

[국면 근거 — 실제 역사]
  닷컴(2000~02)   : 갈아내리는 긴 하락 → 역발상 칼날 잡기 지옥, 드로다운 스탑 천국
  금융위기(2008)  : 시스템 붕괴 대폭락 → 방어형 천국, 공격형 지옥
  코로나(2020)    : V자 급락 후 즉시 회복 → 버티면 생존
  상승장(2017)    : 저변동성 꾸준한 우상향 → 추세추종/모멘텀 강세
  횡보장(2015~16) : 방향 없이 출렁이는 장 → 추세형 헛신호, 평균회귀 유리

[주의] post-COVID(2020-07 이후)는 hold-out — 훈련 체육관으로 추가 금지 (NSGA-III 최종판정용).
"""
from ..core.models import Gym

# SPY는 1993년, QQQ는 1999년 상장. 닷컴은 사용자 실투자 자산(나스닥100)으로 재현한다.
GYMS = [
    # 갈아내리는 긴 하락 — 워크포워드 2000년 폴드(역발상 칼날)가 추가 근거.
    # QQQ로 잰다: 같은 국면의 가장 가혹한 버전(-80%)이자 실투자 자산.
    Gym("2000-02 닷컴 붕괴 체육관", difficulty=10, volatility=8,
        ticker="QQQ", start="2000-03-01", end="2002-12-31"),

    # 시스템 붕괴 대폭락 (방어 못하면 박살)
    Gym("2008 금융위기 체육관", difficulty=10, volatility=10,
        ticker="SPY", start="2008-01-01", end="2009-06-30"),

    # V자 급락 후 즉시 회복 (버티면 생존, 단 변동성 최강)
    Gym("2020 코로나 급락 체육관", difficulty=9, volatility=10,
        ticker="SPY", start="2020-02-01", end="2020-06-30"),

    # 저변동성 꾸준한 우상향 (추세추종/모멘텀의 무대)
    Gym("2017 불타입 상승장 체육관", difficulty=5, volatility=3,
        ticker="SPY", start="2017-01-01", end="2017-12-31"),

    # 방향 없는 횡보 (추세형엔 헛신호 지옥, 평균회귀 유리)
    Gym("2015-2016 횡보장 체육관", difficulty=6, volatility=6,
        ticker="SPY", start="2015-01-01", end="2016-12-31"),
]


def all_gyms() -> list[Gym]:
    """전체 체육관 목록의 '복사본'을 돌려준다(원본 GYMS 보호용)."""
    return list(GYMS)
